CREATE TABLE "EAGLE"."DUMPDATA" 
(
	"ID" NUMBER(13,0) PRIMARY KEY, 
	"NAME" VARCHAR2(50), 
	"SALARY" NUMBER(13,0) 
);

DECLARE
  NUM integer;
BEGIN  
  FOR NUM IN 1..200000 LOOP   
	INSERT INTO DUMPDATA(ID,NAME,SALARY) VALUES(:NUM,'EMPLOYEE'||:NUM,ROUND(DBMS_RANDOM.VALUE(12000,50000)));
  END LOOP;
END;
/
